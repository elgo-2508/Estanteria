const emojiContainer = document.getElementById('emojiContainer');
emojis.forEach((emojiObj, index) => {
  const emojiElement = document.createElement('span');
  emojiElement.classList.add('emoji');
  emojiElement.dataset.category = emojiObj.category;
  emojiElement.innerHTML = emojiObj.emoji;
  emojiElement.addEventListener('click', function() {
      document.getElementById('selectedEmoji').innerHTML = this.innerHTML;
      document.getElementById('emojiValue').value = document.getElementById('selectedEmoji').innerHTML;

      document.getElementById('emojiModal').style.display = 'none'; // Cierra la modal al seleccionar un emoji
  });

  // Agrega emojis a grupos de 8
  if (index % 8 === 0) {
      const row = document.createElement('div');
      row.classList.add('emoji-row');
      emojiContainer.appendChild(row);
  }

  const currentRow = emojiContainer.lastElementChild;
  currentRow.appendChild(emojiElement);
});

document.querySelectorAll('.filterButton').forEach(button => {
  button.addEventListener('click', function() {
      const category = this.dataset.category;
      document.querySelectorAll('.emoji').forEach(emoji => {
          emoji.style.display = category === 'all' || emoji.dataset.category === category ? 'inline-block' : 'none';
      });
  });
});


